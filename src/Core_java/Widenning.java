package Core_java;

public class Widenning {

	public static void main(String[] args) {
		/*Widenning is one of the type of typecasting where smaller datatype will 
		 * be converted into larger datatype*/
		byte b=127;
		int a=b;
		System.out.println("The value of byte b:"+b);
		System.out.println("The value of int a:"+a);
		int num=25;
		float n=num;
		System.out.println("The value of int num:"+num);
		System.out.println("The value of float n:"+n);
	}
}
